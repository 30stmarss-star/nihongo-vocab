/**
 * 입력 글자(들)를 한국식 정자로 변환한 결과를 반환.
 * 신자체가 하나도 없어 변환할 게 없으면(=동일) null.
 */
export function tradForm(s: string): string | null {
  let changed = false;
  const out = [...s]
    .map((c) => {
      const t = SHINJITAI_TO_TRAD[c];
      if (t && t !== c) {
        changed = true;
        return t;
      }
      return c;
    })
    .join("");
  return changed ? out : null;
}
